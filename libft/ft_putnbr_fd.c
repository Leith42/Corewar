#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int v;
	int c;
	int z;

	z = 1000000000;
	c = n;
	if (n == -2147483648)
	{
		write(fd, "-2", 2);
		c = 147483648;
	}
	if (c < 0)
	{
		c = -n;
		ft_putchar_fd('-', fd);
	}
	while (z > 1)
	{
		v = c / z + '0';
		if (v >= '1' || c < n || (n < 0 && c < n * -1))
			ft_putchar_fd(v, fd);
		c = c % z;
		z = z / 10;
	}
	ft_putchar_fd(c + '0', fd);
}
