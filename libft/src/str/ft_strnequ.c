#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t n2;

	n2 = 0;
	if (!s1 || !s2)
		return (-1);
	while (n2 < n)
	{
		if (s1[n2] != s2[n2])
			return (0);
		n2++;
	}
	return (1);
}
