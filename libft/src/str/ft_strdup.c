#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	char	*s2;
	int		n;

	n = 0;
	while (s1[n])
		n++;
	if (!(s2 = (char*)malloc(sizeof(*s2) * (n + 1))))
		return (0);
	n = 0;
	while (s1[n])
	{
		s2[n] = s1[n];
		n++;
	}
	s2[n] = '\0';
	return (s2);
}
