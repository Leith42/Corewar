#include "libft.h"

void	ft_putnbr(int n)
{
	int v;
	int c;
	int z;

	z = 1000000000;
	c = n;
	if (n == -2147483648)
	{
		write(1, "-2", 2);
		c = 147483648;
	}
	if (c < 0)
	{
		c = -n;
		ft_putchar('-');
	}
	while (z > 1)
	{
		v = c / z + '0';
		if (v >= '1' || c < n || (n < 0 && c < n * -1))
			ft_putchar(v);
		c = c % z;
		z = z / 10;
	}
	ft_putchar(c + '0');
}
