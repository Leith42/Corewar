char	*ft_strchr(const char *s, int c)
{
	int n;

	n = 0;
	if (s[n] == c)
		return (char *)(&s[n]);
	while (s[n])
	{
		n++;
		if (s[n] == c)
			return (char *)(&s[n]);
	}
	return (0);
}
