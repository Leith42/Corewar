#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int n;
	int i;

	i = 0;
	n = 0;
	if (s && (*f))
	{
		while (s[n])
		{
			(*f)(&s[n]);
			n++;
		}
	}
}
