int		ft_atoi(const char *str)
{
	int n;
	int nb;
	int sign;

	sign = 1;
	n = 0;
	nb = 0;
	while (str[n] == 32 || (str[n] >= 9 && str[n] <= 13))
		n++;
	if (str[n] == '-' || str[n] == '+')
		if (str[n++] == '-')
			sign = -1;
	while (str[n] >= 48 && str[n] <= 57)
	{
		nb = nb * 10 + (str[n] - 48);
		n++;
	}
	return (nb * sign);
}
