#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t n1;
	size_t n2;

	n1 = ft_strlen(s1);
	n2 = 0;
	while (n2 < n && s2[n2])
	{
		s1[n1] = s2[n2];
		n1++;
		n2++;
	}
	s1[n1] = '\0';
	return (s1);
}
