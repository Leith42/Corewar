#include "libft.h"

int		ft_power(int nb, int power)
{
	if (nb == -1 || nb == 1)
		return ((power % 2 == 0 || nb == 1) ? 1 : -1);
	if (power > 0)
		return (ft_power(nb, --power) * nb);
	if (power == 0)
		return (1);
	return (0);
}
