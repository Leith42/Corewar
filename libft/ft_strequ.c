int		ft_strequ(char const *s1, char const *s2)
{
	int n;

	n = 0;
	if (!s1 || !s2)
		return (-1);
	while (s1[n])
	{
		if (s1[n] != s2[n])
			return (0);
		n++;
	}
	if (!s2[n])
		return (1);
	return (0);
}
